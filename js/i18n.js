export function t(key) {
  return translations[state.language]?.[key] || translations.en[key] || key;
}

const RTL_LANGUAGES = ['ar'];
const SUPPORTED_LANGUAGES = ['en', 'fr', 'ar', 'de'];

export function isRTL(lang) {
  return RTL_LANGUAGES.includes(lang || state.language);
}

export function setLanguage(lang) {
  if (!SUPPORTED_LANGUAGES.includes(lang)) return;
  state.language = lang;

  // Store preference
  try {
    sessionStorage.setItem('petronox-lang', lang);
  } catch (e) {}

  // Apply RTL/LTR direction
  const dir = isRTL(lang) ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('dir', dir);
  document.documentElement.setAttribute('lang', lang);
  document.body.classList.toggle('rtl', isRTL(lang));

  // Update data-i18n elements
  $$('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);
    if (el.tagName === 'INPUT') {
      el.placeholder = translation;
    } else {
      el.textContent = translation;
    }
  });

  // Notify all listeners
  notify('languageChanged', lang);
}

export function initI18n() {
  // Restore saved language
  try {
    const saved = sessionStorage.getItem('petronox-lang');
    if (SUPPORTED_LANGUAGES.includes(saved)) {
      state.language = saved;
    }
  } catch (e) {}

  // Apply initial direction
  const dir = isRTL(state.language) ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('dir', dir);
  document.documentElement.setAttribute('lang', state.language);
  document.body.classList.toggle('rtl', isRTL(state.language));
}

export function getLanguage() {
  return state.language;
}

